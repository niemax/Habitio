export const renderIconBackgroundColor = (color) => {
    switch (color) {
        case '#0FCBFA':
            return 'blue';
        case '#3F3FBF':
            return 'indigo';
        case '#EA5B71':
            return 'rose';
        case '#E2FA0F':
            return 'yellow';
        case '#FA9C0F':
            return 'orange';
        case '#0F84FA':
            return 'blue';
        case '#7D6C06':
            return 'yellow';
        case '#FF4040':
            return 'red';
        case '#2EB284':
            return 'tertiary';
        case '#0F84FA':
            return 'blue';
        case '#7D6C06':
            return 'yellow';
        case '#FF4040':
            return 'red';
        case '#FF6347':
            return 'orange';
        case '#FA0FE2':
            return '#db2777';
        case '#02D94D':
            return 'lime';
        default:
            return 'indigo';
    }
};
